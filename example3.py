# Range of Indexes
# Return the third, fourth and fifth item:

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:5])

print (thislist[:4])

print (thislist[2:])

# Range of Negative Indexes
# This example returns the items from "orange" (-4) to, but NOT including "mango" (-1):

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print (thislist[-4:-1])

