# Insert Items

thislist = ["apple", "banana", "cherry"]
thislist.insert (2, "watermelon")
print (thislist)

# Append Items

thislist = ["apple", "banana", "cherry"]
thislist.append ("orange")
print (thislist)

#Extend list
# Add the elements of tropical to thislist:

thislist = ["apple", "banana", "cherry"]
tropical = ["mango", "pineapple", "papaya"]
thislist.extend(tropical)
print (thislist)

